const color = document.querySelector(".color");
const ColorInput = document.getElementById("ColorInput");
const Description = document.getElementById("Description");

btn.addEventListener("click", function () {
    let inputColor = ColorInput.value.trim();

    if(isValidHex(inputColor)){
        color.textContent = inputColor;
        document.body.style.backgroundColor = inputColor;
        Description.textContent = getDiscription(inputColor);
    }
    if(inputColor == 0){
        let hexColor = getRandomColor()
        color.textContent = hexColor;
        document.body.style.backgroundColor = hexColor;
        Description.textContent = getDiscription(hexColor);
    }else if(!isValidHex(inputColor)){
        alert("Please enter a valid hex color");
    }

});

function getRandomColor(){
    let hexColor = "#";
    for (let i = 0; i < 6; i++) {
        hexColor += hex[getRandomNumber()];
    }
    return hexColor;
}
function getRandomNumber() {
  return Math.floor(Math.random() * hex.length);
}

function isValidHex(color) {
    const hexRegex = /^#([0-9A-Fa-f]{3}){1,2}$/i;
    return hexRegex.test(color);
}


